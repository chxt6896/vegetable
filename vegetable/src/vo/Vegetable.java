package vo;

public class Vegetable {
	
	private String id;	
	private String sid;
	private String vid;				//序号
	private String name;			//名字
	private String names;			//名字+别名
	private String techang;			//特长
	private String aihao;			//爱好
	private String demo;			//简述
	private String content;			//single页面框框里待发布的文字内容
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getSid() {
		return sid;
	}
	public void setSid(String sid) {
		this.sid = sid;
	}
	public String getVid() {
		return vid;
	}
	public void setVid(String vid) {
		this.vid = vid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getNames() {
		return names;
	}
	public void setNames(String names) {
		this.names = names;
	}
	public String getTechang() {
		return techang;
	}
	public void setTechang(String techang) {
		this.techang = techang;
	}
	public String getAihao() {
		return aihao;
	}
	public void setAihao(String aihao) {
		this.aihao = aihao;
	}
	public String getDemo() {
		return demo;
	}
	public void setDemo(String demo) {
		this.demo = demo;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	
}
